DROP TABLE IF EXISTS jeu CASCADE;
DROP TABLE IF EXISTS joueur CASCADE;
DROP TABLE IF EXISTS partie CASCADE;
DROP TABLE IF EXISTS participe CASCADE;

CREATE TABLE jeu (
	idjeu serial primary key,
	titre varchar(50) NOT NULL,
	nbmin int,
	nbmax int,
	est_extension int references jeu(idjeu)
);

CREATE TABLE joueur (
	pseudo varchar(25) primary key,
	nom varchar(25),
	prenom varchar(25),
	naissance date,
	sexe char(1)
);

CREATE TABLE partie (
	idpartie serial primary key,
	idjeu int references jeu(idjeu),
	organisateur varchar(25) references joueur(pseudo),
	statut varchar(10)
);

CREATE TABLE participe (
	pseudo varchar(25) references joueur(pseudo),
	idpartie int references partie(idpartie),
	rang int,
	primary key (pseudo, idpartie)
);


